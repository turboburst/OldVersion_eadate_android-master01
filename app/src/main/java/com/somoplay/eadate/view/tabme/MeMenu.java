public class MeMenu {

    int image;
    String meMenu;

    public MeMenu(int image, String meMenu) {
        this.image = image;
        this.meMenu = meMenu;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getMeMenu() {
        return meMenu;
    }

    public void setMeMenu(String meMenu) {
        this.meMenu = meMenu;
    }

}
